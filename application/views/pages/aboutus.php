            <p>Bannering the Sogo Cares program is the Doctors-on-Wheels (DOW) that focuses on providing free medical consultations, 
            laboratory examinations and medications to underserved communities throughout the Philippines. <br><br> The DOW is a 
            mobile medical air-conditioned bus that is equipped and furnished with examining beds, 10 examining chairs, 
            medical/examination lights and other equipment to be used for medical missions. <br><br>
            The DOW already reached and provided medical assistance to remote areas of Pangasinan, Pampanga, Nueva Ecija, Tarlac, 
            Bataan, Zambales, Bicol and Iloilo. <br><br>
            It is manned by volunteer doctors, nurses, optometrists, pharmacists, social workers from partner LGUs and and NGOs, 
            and employees of Hotel Sogo fulfilling their part to help the needy.</p>
